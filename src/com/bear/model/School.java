public class School {
    int school_id;
    String school_name;
    String school_website;
    String school_tel;
    String school_other;
    int school_type;

    public int getSchool_id() {
        return school_id;
    }

    public void setSchool_id(int school_id) {
        this.school_id = school_id;
    }

    public String getSchool_name() {
        return school_name;
    }

    public void setSchool_name(String school_name) {
        this.school_name = school_name;
    }

    public String getSchool_website() {
        return school_website;
    }

    public void setSchool_website(String school_website) {
        this.school_website = school_website;
    }

    public String getSchool_tel() {
        return school_tel;
    }

    public void setSchool_tel(String school_tel) {
        this.school_tel = school_tel;
    }

    public String getSchool_other() {
        return school_other;
    }

    public void setSchool_other(String school_other) {
        this.school_other = school_other;
    }

    public int getSchool_type() {
        return school_type;
    }

    public void setSchool_type(int school_type) {
        this.school_type = school_type;
    }
}
